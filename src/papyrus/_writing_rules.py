"""Writing rules — single source of truth for Papyrus parser syntax."""

from __future__ import annotations


# ---------------------------------------------------------------------------
# Universal syntax reference (injected into every template guide)
# ---------------------------------------------------------------------------

SYNTAX_REFERENCE = """\

---

## Papyrus 마크다운 문법 레퍼런스

이 레퍼런스는 파서가 인식하는 모든 구문을 정의합니다.
여기에 없는 구문은 렌더링되지 않거나 예상치 못한 결과를 만듭니다.

### 섹션 구조
- `##` = 섹션, `###` = 서브섹션 (선택)
- `####` 이상은 렌더링되지 않습니다

### 칼아웃 블록
`> [!TYPE] 본문` 형식으로 작성합니다. TYPE과 본문은 반드시 같은 줄에 씁니다.

4종 타입:
- `> [!info]` — 정보 (파랑 ℹ): 참고 정보, 배경 설명
- `> [!tip]` — 권장 (초록 ✔): 유용한 제안, 권장 사항
- `> [!warning]` — 경고 (주황 ⚠): 주의가 필요한 사항
- `> [!danger]` — 위험 (빨강 ✕): 즉각 조치가 필요한 사항

규칙:
- 한 칼아웃 = 한 줄. 여러 줄이면 별도 칼아웃으로 분리
- 인라인 마크다운 사용 가능: **볼드**, *이탤릭*, `코드`
- `[!TYPE]` 마커는 맨 앞에만 위치 — 본문 텍스트 안에 넣지 않음
- `> [!TYPE]` 다음 줄에 `>` 연속 불가 (무시됨)

### 인용 블록
`>` 인용 블록은 작성자의 판단·해석·시사점 전용입니다.
수치·사실 나열에는 사용하지 않습니다 (자동으로 일반 단락 변환).

### 리스트 항목
구분자는 반드시 `: ` 를 사용합니다. ` — ` 는 금지입니다.
(CSS가 자동으로 앞에 '—' 마커를 추가합니다.)

### 표
마크다운 파이프 테이블을 사용합니다.
첫 번째 열은 자동으로 라벨 스타일(볼드, 중앙정렬)이 적용됩니다.

### 차트
표 바로 아래 줄에 `<!-- chart:TYPE -->` 주석을 추가하면 차트로 렌더링됩니다.
- `chart:bar` — 비교·순위 (멀티 열 자동 grouped bar)
- `chart:line` — 추이·트렌드
- `chart:pie` — 비율·구성 (열 1개면 pie, 2개+ 면 doughnut)
- `chart:gantt` — 일정 (열 순서: 항목 | 시작(주) | 기간(주))

### 다이어그램
` ```mermaid ` 코드블록으로 다이어그램을 삽입합니다.
- `flowchart LR` — 가로 프로세스 흐름
- `flowchart TD` — 세로 의사결정 흐름
- `sequenceDiagram` — 시스템·참여자 간 통신
- `mindmap` — 개념·구조 방사형 정리

### 이미지
전체 너비: `![캡션](경로)` 또는 `![캡션](https://...)`
좌측 이미지 + 우측 텍스트:
```
<!-- img:left -->
![캡션](./파일명.png)
우측 설명 텍스트
<!-- /img -->
```
우측 이미지 + 좌측 텍스트:
```
<!-- img:right -->
![캡션](./파일명.png)
좌측 설명 텍스트
<!-- /img -->
```

### 각주
인라인 참조: `[^id]` 형태로 본문에 삽입합니다.
각주 정의: `[^id]: 내용` 을 해당 섹션 끝에 작성합니다.
출처 표기와 보충 설명 모두 가능합니다.
각주는 자동으로 문서 마지막 '참고문헌' 페이지에 모아 표시됩니다.

예시:
```
시장 규모는 연 12% 성장 중[^src1]이며, 국내는 8%[^src2] 수준입니다.

[^src1]: McKinsey Global Report, 2024
[^src2]: 한국IDC 연간 보고서, Q3 2024
```
"""


# ---------------------------------------------------------------------------
# Component guides (used by start_report prompt)
# ---------------------------------------------------------------------------

COMPONENT_GUIDES = {
    "callout": (
        "- 정보(파랑): `> [!info] 참고 정보나 배경 설명`\n"
        "- 권장(초록): `> [!tip] 유용한 제안이나 권장 사항`\n"
        "- 경고(주황): `> [!warning] 주의가 필요한 내용`\n"
        "- 위험(빨강): `> [!danger] 즉각 조치가 필요한 내용`\n"
        "- 한 줄만 지원 — 여러 줄이면 칼아웃을 각각 따로 작성\n"
        "- 인라인 마크다운(**볼드**, *이탤릭*, `코드`) 사용 가능"
    ),
}


# ---------------------------------------------------------------------------
# Builder
# ---------------------------------------------------------------------------

def build_writing_guide(template_notes: str) -> str:
    """Combine template-specific tips with universal syntax reference."""
    parts: list[str] = []
    if template_notes.strip():
        parts.append(template_notes.rstrip())
    parts.append(SYNTAX_REFERENCE)
    return "\n\n".join(parts)
